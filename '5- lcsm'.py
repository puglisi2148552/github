def parse_fasta(fasta_data):
    """Parse FASTA formatted input and return a list of sequences."""
    sequences = []
    current_seq = []
    for line in fasta_data.strip().splitlines():
        line = line.strip()
        if line.startswith(">"):
            if current_seq:
                sequences.append("".join(current_seq))
                current_seq = []
        else:
            current_seq.append(line)
    if current_seq:
        sequences.append("".join(current_seq))
    return sequences

def is_common_substring(sub, sequences):
    """Check if a substring is present in all sequences."""
    return all(sub in seq for seq in sequences)

def longest_common_substring(sequences):
    """Find the longest common substring among all DNA sequences."""
    # Use the shortest sequence as a reference to reduce search space
    shortest_seq = min(sequences, key=len)
    left, right = 0, len(shortest_seq)
    best_substr = ""
    
    while left <= right:
        mid = (left + right) // 2
        found = False
        # Check all substrings of length `mid` in the reference sequence
        for i in range(len(shortest_seq) - mid + 1):
            substring = shortest_seq[i:i + mid]
            if is_common_substring(substring, sequences):
                best_substr = substring
                found = True
                break
        if found:
            # If a common substring of length `mid` is found, try for a longer one
            left = mid + 1
        else:
            # If not found, try for a shorter one
            right = mid - 1
            
    return best_substr

