def parse_fasta(fasta_data):
    """Parse FASTA formatted input and return a list of sequences."""
    sequences = []
    current_seq = []
    for line in fasta_data.strip().splitlines():
        line = line.strip()
        if line.startswith(">"):
            if current_seq:
                sequences.append("".join(current_seq))
                current_seq = []
        else:
            current_seq.append(line)
    if current_seq:
        sequences.append("".join(current_seq))
    return sequences

def create_profile_matrix(sequences):
    """Create a profile matrix from the list of DNA sequences."""
    length = len(sequences[0])
    profile = {nucleotide: [0] * length for nucleotide in "ACGT"}
    
    for seq in sequences:
        for index, nucleotide in enumerate(seq):
            profile[nucleotide][index] += 1
            
    return profile

def create_consensus_string(profile):
    """Create a consensus string from the profile matrix."""
    consensus = []
    for i in range(len(next(iter(profile.values())))):
        max_nucleotide = max(profile, key=lambda x: profile[x][i])
        consensus.append(max_nucleotide)
    
    return ''.join(consensus)

